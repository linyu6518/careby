// 简单的繁体转换函数（仅转换映射表中的词汇）
export function convertToTraditional(text: string): string {
  let result = text;
  
  // 按词长度排序，优先替换长词
  const sortedKeys = Object.keys(s2tMap).sort((a, b) => b.length - a.length);
  
  for (const key of sortedKeys) {
    const regex = new RegExp(key, 'g');
    result = result.replace(regex, s2tMap[key]);
  }
  
  return result;
}

// 转换对象中的所有字符串
export function convertObjectToTraditional(obj: any): any {
  if (typeof obj === 'string') {
    return convertToTraditional(obj);
  }
  
  if (Array.isArray(obj)) {
    return obj.map(item => convertObjectToTraditional(item));
  }
  
  if (obj && typeof obj === 'object') {
    const result: any = {};
    for (const key in obj) {
      result[key] = convertObjectToTraditional(obj[key]);
    }
    return result;
  }
  
  return obj;
}
